def area(a, h): 
    '''Принимает 2 значения, a и h. a - сторона треугольника, h - высота проведенная к стороне a. Возвращает площадь тереугольника.'''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает 3 значения, a, b и c. a - первая сторона треугольника, b - вторая сторона треугольника, c - третья сторона треугольника. Возвращает периметр треугольника.'''
    return a + b + c 